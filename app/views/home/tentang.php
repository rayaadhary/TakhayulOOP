                    <p>Merujuk ke pengertian kata “takhayul” dalam Kamus Besar Bahasa Indonesia (KBBI), takhayul adalah suatu hal yang tidak nyata atau hanya ada dalam khayalan saja.</p>

                    <p>Takhayul sendiri dapat diklasifikasikan dalam tiga kelompok, yaitu agama, budaya, dan individual, Sobat. Dalam sebuah agama, takhayul bisa muncul dalam bentuk ritual-ritual atau kebiasaan yang diyakini baik dan perlu untuk dilakukan. Karena sifat takhayul yang sering dianggap tidak masuk akal, orang mungkin menganggap hal-hal yang dilakukan oleh orang yang beragama lain darinya itu “hanya takhayul.”</p>

                    <p>Dalam budaya, kita bisa juga menemukan kebiasaan-kebiasaan turun-temurun untuk mencegah kemalangan, menghilangkan sakit, membawa kebaikan, dan lain sebagainya.</p>

                    <p>Seseorang juga bisa memunculkan takhayul nya masing-masing, lho. Misalnya percaya bahwa ia memiliki jimat keberuntungan. Lalu, kalau mau mengikuti suatu acara khusus, ia akan selalu mengenakan jimat itu supaya dijauhkan dari hal buruk misalnya.</p>

                    <p>Setiap orang atau kelompok sangat mungkin memiliki keyakinan yang berbeda-beda karena sifat takhayul yang subjektif. Itu kenapa apa yang menjadi kepercayaan kita belum tentu dipercaya oleh orang lain, terutama yang memiliki latar belakang budaya yang berbeda.</p>

                    <p>Sejalan dengan anggapan takhayul yang tidak masuk akal, pada laman The Open University, takhayul didefinisikan sebagai kepercayaan bahwa suatu hal terjadi bukan karena penyebab alaminya, melainkan karena sihir, kebetulan, atau bantuan ilahi. </p>

                    <p>Walaupun begitu, Uniawari (2012) dalam tulisannya untuk Balai Pelestarian Sejarah dan Nilai Tradisional Bandung menyampaikan bahwa takhayul memiliki fungsi sosial yang penting, lho. Menurutnya, takhayul digunakan oleh sebuah komunitas untuk menjaga nilai-nilai yang berlaku dalam masyarakatnya.</p>

                    <p>Oleh karena itu, biasanya dalam konteks kebudayaan, takhayul cenderung menggambarkan suatu larangan atau hal-hal yang “tabu”. Masyarakat pun melestarikan takhayul itu untuk menjaga nilai budaya-nya tetap baik.</p>
